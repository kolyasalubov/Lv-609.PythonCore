from math import pi

def square_rectangle(length, width):
    """
    This function calculates the square of rectangle.
    """
    return(length * width)

def square_triangle(height, base):
    """
    This function calculates the square of triangle.
    """
    return((height * base) / 2)

def square_circle(radius):
    """
    This function calculates the square of circle.
    """
    return(pi * radius**2)
